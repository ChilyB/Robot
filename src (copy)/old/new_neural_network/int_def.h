#ifndef _INT_DEF_H_
#define _INT_DEF_H

#ifndef u32
typedef unsigned int u32 ;
#endif

#ifndef u16
typedef unsigned short int u16 ; 
#endif

#ifndef u8
typedef unsigned char u8 ;
#endif

#ifndef i32
typedef signed int i32 ;
#endif

#ifndef i16
typedef signed short int i16 ;
#endif

#ifndef i8
typedef signed char i8 ;
#endif


#endif